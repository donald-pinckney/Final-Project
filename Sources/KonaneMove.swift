import Foundation

/*
KonaneMove
x	- init(fromX: Int, fromY: Int, toX: Int, toY: Int)
x	- fromX
x	- fromY
x	- toX
x	- toY
*/

class KonaneMove {

	let fromX: Int
	let fromY: Int
	let toX: Int
	let toY: Int

	init (fromX: Int, fromY: Int, toX: Int, toY: Int) {
		self.fromX = fromX
		self.fromY = fromY
		self.toX = toX
		self.toY = toY
	}
}
